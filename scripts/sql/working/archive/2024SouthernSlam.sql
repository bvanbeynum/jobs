select	Team
		, TeamRecord = cast(sum(wins) over (partition by team) as varchar(255)) + ' - ' + cast(sum(Losses) over (partition by team) as varchar(255))
		, WeightClass
		, WrestlerName
		, Seed
		, Record = cast(Wins as varchar(255)) + ' - ' + cast(Losses as varchar(255))
		, BestWin
		, WinDiff = Seed - BestWin
		, WorstLoss
		, LossDiff = Seed - WorstLoss
		, Place
		, Result = case 
			when BestWin < Seed then 'Better'
			when Place < Seed then 'Better'
			when WorstLoss < Seed then 'Expected'
			when Place = Seed then 'Expected'
			else 'Worse' end
		, TeamLosses = sum(Losses) over (partition by team)
		, TeamWins = sum(wins) over (partition by team)
into	#TeamResult
from	(
		select	TrackWrestlerMatch.Team
				, TrackMatch.WeightClass
				, TrackWrestler.WrestlerName
				, WrestlerSeed.Seed
				, Wins = sum(case when TrackWrestlerMatch.IsWinner = 1 then 1 else 0 end)
				, Losses = sum(case when TrackWrestlerMatch.IsWinner = 0 then 1 else 0 end)
				, BestWin = min(case when TrackWrestlerMatch.IsWinner = 1 then OpponentSeed.Seed else null end)
				, WorstLoss = max(case when TrackWrestlerMatch.IsWinner = 0 then OpponentSeed.Seed else null end)
				, Place = max(case 
					when TrackMatch.RoundName = '1st Place Match' and TrackWrestlerMatch.IsWinner = 1 then 1
					when TrackMatch.RoundName = '1st Place Match' and TrackWrestlerMatch.IsWinner = 0 then 2
					when TrackMatch.RoundName = '3rd Place Match' and TrackWrestlerMatch.IsWinner = 1 then 3
					when TrackMatch.RoundName = '3rd Place Match' and TrackWrestlerMatch.IsWinner = 0 then 4
					when TrackMatch.RoundName = '5th Place Match' and TrackWrestlerMatch.IsWinner = 1 then 5
					when TrackMatch.RoundName = '5th Place Match' and TrackWrestlerMatch.IsWinner = 0 then 6
					when TrackMatch.RoundName = '7th Place Match' and TrackWrestlerMatch.IsWinner = 1 then 7
					when TrackMatch.RoundName = '7th Place Match' and TrackWrestlerMatch.IsWinner = 0 then 8
					else null end)
		from	TrackEvent
		join	TrackMatch
		on		TrackEvent.ID = TrackMatch.TrackEventID
		join	TrackWrestlerMatch
		on		TrackMatch.ID = TrackWrestlerMatch.TrackMatchID
		join	TrackWrestler
		on		TrackWrestlerMatch.TrackWrestlerID = TrackWrestler.ID
		join	#seeds WrestlerSeed
		on		TrackWrestler.WrestlerName = WrestlerSeed.WrestlerName
		join	TrackWrestlerMatch OpponentMatch
		on		TrackWrestlerMatch.TrackMatchID = OpponentMatch.TrackMatchID
				and TrackWrestlerMatch.TrackWrestlerID <> OpponentMatch.TrackWrestlerID
		join	TrackWrestler Opponent
		on		OpponentMatch.TrackWrestlerID = Opponent.ID
		join	#seeds OpponentSeed
		on		Opponent.WrestlerName = OpponentSeed.WrestlerName
		where	TrackEvent.ID = 8927
				-- and TrackWrestlerMatch.Team = 'fort mill'
		group by
				TrackWrestlerMatch.Team
				, TrackMatch.WeightClass
				, TrackWrestler.WrestlerName
				, WrestlerSeed.Seed
		) WrestlerPlace


